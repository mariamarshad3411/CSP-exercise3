// Part 1: Variables and Constants
let age = 25; // Variable to store age
const height = 5.8; // Constant to store height (can't be changed)

// Part 2: Arrays
let pets = ['Bella', 'Charlie', 'Max']; // Initial array with pet names

// Add a new pet to the array
pets.push('Lucy');

// Remove the last pet from the array
pets.pop();

// Reverse the array
pets.reverse();

// Display the updated array in the console
console.log(pets); // This will show the reversed array in the console

// Part 3: Objects
let car = {
    make: 'Toyota',
    model: 'Corolla',
    year: 2020
};

// Update the year of the car
car.year = 2021;

// Add a new property to the car object
car.color = 'blue';

// Log the updated object to the console
console.log(car); // This will show the updated car object in the console
